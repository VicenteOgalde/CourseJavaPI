package OOP;

public class Car {
	
	private int wheels;
	private int length;
	private int width;
	private int motor;
	private int weight;
	
	public Car(int wheels, int length, int width, int motor, int weight) {
		this.wheels=wheels;
		this.length=length;
		this.width=width;
		this.motor=motor;
		this.weight=weight;
	}

}
